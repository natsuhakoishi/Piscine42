#include <unistd.h>

int	ft_str_is_lowercase(char *str)
{
	int	a;

	a = 1;
	while (*str != '\0')
	{
		if (!(*str >= 'a' && *str <= 'z'))
		{
			a = 0;
			break ;
		}
		str++;
	}
	return (a);
}
